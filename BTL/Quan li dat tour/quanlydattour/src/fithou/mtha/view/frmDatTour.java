    /**
     * doc du lieu tu lop KhachHangDAO
     * hien danh sach vao cboKhach
     */
    private void loadCboKhach() throws IOException, FileNotFoundException, ClassNotFoundException{
        ArrayList<KhachHang> lsKH = new ArrayList<KhachHang>();
        lsKH = khDAO.docDSKH();
        KhachHangModel khModel = new KhachHangModel(lsKH);
        cboKhach.setModel(khModel);
        cboKhach.setSelectedIndex(0);
        cboKhach.setRenderer(new DefaultListCellRenderer(){
            @Override
            public Component getListCellRendererComponent(JList<?> jlist, Object o, int i, boolean bln, boolean bln1) {
               super.getListCellRendererComponent(jlist, o, i, bln, bln1); 
               if(o instanceof KhachHang){
                   KhachHang kh = (KhachHang)o;
                   setText(kh.getHoTen());
               }
               return this;
            }        
        
        });
    }
    
    private DatTour getDatTour(){
        DatTour dt = new DatTour();
        dt.setKh((KhachHang)cboKhach.getSelectedItem());
        dt.setTour((Tour) cboTour.getSelectedItem());
        dt.setNgayDat(Integer.parseInt(txtSoNgay.getText()));
        return dt;
    }
