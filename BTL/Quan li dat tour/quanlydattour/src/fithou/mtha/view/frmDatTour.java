    private void loadDSTour() throws IOException, FileNotFoundException, ClassNotFoundException{
        //doc du lieu tu file tour.dat
        ArrayList<Tour> lsTour = tourDAO.docDSTour();
        //gan ds cho tourmodel
        TourModel tourModel = new TourModel(lsTour);
        //set tourModel cho cboTour
        cboTour.setModel(tourModel);
        cboTour.setSelectedIndex(0);
        cboTour.setRenderer(new DefaultListCellRenderer(){
            @Override
            public Component getListCellRendererComponent(JList<?> jlist, Object o, int i, boolean bln, boolean bln1) {
                super.getListCellRendererComponent(jlist, o, i, bln, bln1);
                if(o instanceof Tour){
                    Tour tour = (Tour)o;
                    setText(tour.getThongTinTour());
                }
                
                return this;
            }
            
        
        });
    }
